# type casting - convert the datatype of a value to another datatype

# any to string
a = 1
b = 2.0
c = '3'

x = str(a)
y = str(b)
z = str(c)

print(x)
print(y)
print(z*4)
print(x + ', ' + y + ', ' + z)

print('')
# any to int
b = int(b)
c = int(c)

print(a)
print(b)
print(c)

print('')
# any to float
a = float(a)
b = float(b)
c = float(c)

print(a)
print(b)
print(c)
