import math

pi = 3.1414
pi_n = -3.1414
a = 10
b = 20
c = 30

# round off the value
print(round(pi))

# round off to the nearest top(ceiling) value
print(math.ceil(pi))

# round off to the nearest low(floor) value
print(math.floor(pi))

# give absolute value
print(abs(pi_n))

# gives power
print(pow(a, 2))

# gives square root
print(math.sqrt(144))

# gives maximum
print(max(a, b, c))

# gives minimum
print(min(a, b, c))